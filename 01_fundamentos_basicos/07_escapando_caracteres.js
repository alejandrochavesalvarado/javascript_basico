// Escape alternativo
let frase = "I'm Software Enginner";
console.log(frase);

// Escape con barra invertida
frase = 'I\'m Software Enginner';
console.log(frase);

// Escapando con template literals
frase = `I'm Software Enginner`;
console.log(frase);

// Escritura de strings largos
let poema = 'Las rosas son rojas\nLas violetas son azules\nCaracter inesperado';
console.log(poema);
poema = 'Las rosas son rojas\n\
Las violetas son azules\n\
Caracter inesperado';
console.log(poema);
poema = `Las rosas son rojas
Las violetas son azules
Caracter inesperado`;
console.log(poema);